class InserBranchData
{
    public array $data;

    public function __construct()
    {
        $this->data = $this->prepareData();
        SeederCheck::start();
    }

    /**
     * Handle create user with all possible assign [branch-region]
     */
    public function handle(): void
    {
        foreach ($this->data['users']['email'] as $i => $email) {

            // Handle existing branch
            $branch = Branch::where('code', $this->data['branches']['code'][$i])->first();

            if (!$branch) {
                $branch = $this->createBranch([
                    'region' => $this->data['branches']['region'][$i],
                    'name' => $this->data['branches']['name'][$i],
                    'code' => $this->data['branches']['code'][$i],
                ]);
            }

            $user = $this->handleUser([
                'email' => $email,
                'name' => $this->data['users']['name'][$i],
                'mobile' => $this->data['users']['mobile'][$i],
            ]);

            if (!empty($branch)) {
                $branches = $user->branches->pluck('id')->toArray();
                $all_branches = array_values(array_unique(array_merge($branches, [$branch->id])));
                $user->branches()->sync($all_branches);
            }
        }
    }

    /**
     * Create user and assign branch to him
     *
     * @param $data
     * @return mixed
     */
    public function handleUser($data)
    {
        $user_data = [
            'name' => $data['name'],
            'phone' => $data['mobile'],
            'type' => 'subcustomer',
            'password' => '123456789',
            'parent_id' => 3
        ];

        $user = User::updateOrCreate(['email' => $data['email']], $user_data);

        $user->syncRoles('customer');

        return $user;
    }

    /**
     * Create new branch and handle region assign
     *
     * @param $data
     * @return mixed
     */
    public function createBranch($data)
    {
        $region = Region::where('name', $data['region'])->first();

        // Handle region and if not found region create new region
        if (!$region) {
            $region = Region::create([
                'name' => $data['region'],
                'user_id' => 3,
                'parent_id' => 1
            ]);

        }
        $data['region_id'] = $region->id;

        // Create new branch
        $branch = Branch::firstOrCreate([
            'code' => $data['code']
        ], [
            'name' => $data['code'] . " - {$data['name']}",
            'description' => "{$data['name']} Description",
            'user_id' => 3,
            'area_count' => 4,
            'region_id' => $data['region_id'],
        ]);

        // Handle assign modesl to branch
        collect([3, 4])->each(function ($item) use ($branch) {
            UserModelBranch::updateOrCreate([
                'branch_id' => $branch->id,
                'user_model_id' => $item,
            ]);
        });

        // Handle creation areas for branch
        for ($i = 1; $i <= $branch->area_count; $i++) {
            $branch->areas()->firstOrCreate([
                'area' => $i,
                'branch_code' => $branch->code,
            ], [
                'status' => 0,
            ]);
        }

        return $branch;
    }

